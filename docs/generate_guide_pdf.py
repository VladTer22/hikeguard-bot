"""Generate HikeGuard_Guide.pdf — admin-facing guide.

Run from project root:
    .venv/bin/python docs/generate_guide_pdf.py

Output: HikeGuard_Guide.pdf in the project root.
"""

from pathlib import Path

from fpdf import FPDF

TITLE = "HikeGuard — Anti-spam бот"
SUBTITLE = "Інструкція для адміністраторів чату"

FONT_DIR = Path("/usr/share/fonts/TTF")


class GuidePDF(FPDF):
    def header(self) -> None:
        self.set_font("DejaVu", "B", 11)
        self.cell(0, 6, TITLE, align="C", new_x="LMARGIN", new_y="NEXT")
        self.set_font("DejaVu", "I", 9)
        self.cell(0, 5, SUBTITLE, align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(4)

    def footer(self) -> None:
        self.set_y(-15)
        self.set_font("DejaVu", "", 9)
        self.cell(0, 10, str(self.page_no()), align="C")

    def h1(self, text: str) -> None:
        self.ln(2)
        self.set_font("DejaVu", "B", 16)
        self.multi_cell(0, 8, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def h2(self, text: str) -> None:
        self.ln(1)
        self.set_font("DejaVu", "B", 12)
        self.multi_cell(0, 6, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def p(self, text: str) -> None:
        self.set_font("DejaVu", "", 10)
        self.multi_cell(0, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def bullets(self, items: list[str]) -> None:
        self.set_font("DejaVu", "", 10)
        left = self.l_margin
        for item in items:
            # Render bullet glyph, then wrap the item text indented so that
            # continuation lines align with the bullet's body, not the margin.
            self.set_x(left)
            self.cell(5, 5.5, "•")
            with self.local_context():
                self.set_left_margin(left + 5)
                self.set_x(left + 5)
                self.multi_cell(0, 5.5, item, new_x="LMARGIN", new_y="NEXT")
        self.set_left_margin(left)
        self.ln(1)

    def callout(self, text: str) -> None:
        """Italic paragraph for examples — flush-left to match body."""
        self.set_font("DejaVu", "I", 10)
        self.multi_cell(0, 5.5, text, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def cmd(self, name: str, body: str) -> None:
        """Command name in bold, description below. Keeps the bold name
        together with at least the first line of body to avoid orphans."""
        # Reserve room for name + 1 line of body (the true-orphan threshold)
        min_height = 5.5 + 5.5
        if self.get_y() + min_height > self.h - self.b_margin:
            self.add_page()
        self.set_font("DejaVu", "B", 10)
        self.multi_cell(0, 5.5, name, new_x="LMARGIN", new_y="NEXT")
        self.set_font("DejaVu", "", 10)
        self.multi_cell(0, 5.5, body, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)


